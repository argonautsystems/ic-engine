def main() -> int:
    """Main entry point."""
    try:
        # Check if running in non-interactive mode (e.g., from harness)
        if not sys.stdin.isatty():
            print(
                json.dumps(
                    {
                        "status": "skipped",
                        "reason": "LLM config requires interactive mode. Run with -i flag in terminal.",
                        "ic_result": {"script": "llm_config.py", "exit_code": 0},
                    }
                )
            )
            return 0

        if is_claude_code():
            setup_claude_code()
        else:
            setup_openclaw()
        return 0
    except KeyboardInterrupt:
        print("\n\n⏹️  Cancelled.")
        return 1
    except Exception as e:
        print(f"\n❌ Error: {e}")
        logger.exception("llm_config error")
        return 1


if __name__ == "__main__":
    sys.exit(main())
